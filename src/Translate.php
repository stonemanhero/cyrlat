    /**
     * @param string|array $text
     * @param bool $isLatToCyr
     * @return string|array
     */
    public static function process(string|array $text, bool $isLatToCyr=false): string|array
    {
        $map = $isLatToCyr ? self::$latToCyrMap : self::$cyrToLatMap;

        foreach ($map as $key => $value) {
            $text = str_replace($key, $value, $text);
        }

        return $text;
    }
